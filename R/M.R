#' mM
#' @param x numeric
#' @export
mM <- function(x) units::set_units(x, "mmol/L")


#' uM
#' @param x numeric
#' @export
uM <- function(x) units::set_units(x, "umol/L")


#' nM
#' @param x numeric
#' @export
nM <- function(x) units::set_units(x, "nmol/L")

